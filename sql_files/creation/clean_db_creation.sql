CREATE DATABASE IF NOT EXISTS CLEAN;
USE CLEAN;
CREATE TABLE MUMBAI(
ID INTEGER PRIMARY KEY AUTO_INCREMENT,
SELLER_TYPE ENUM('OWNER', 'AGENT', 'BUILDER'), 
BEDROOM FLOAT, 
LAYOUT ENUM ('BHK', 'RK'), 
PROPERTY_TYPE ENUM(
'Independent Floor',
'Apartment',
'Independent House',
'Studio Apartment',
'Villa',
'Penthouse'
), 
LOCALITY VARCHAR(100), 
PRICE FLOAT, 
AREA FLOAT, 
FURNISH_TYPE ENUM('Semi-Furnished', 'Furnished', 'Unfurnished'), 
BATHROOM FLOAT
);


CREATE TABLE DELHI LIKE MUMBAI;
CREATE TABLE KOLKATA LIKE MUMBAI;
CREATE TABLE BANGALORE LIKE MUMBAI;
CREATE TABLE HYDERABAD LIKE MUMBAI;
CREATE TABLE CHENNAI LIKE MUMBAI;
CREATE TABLE AHEMDABAD LIKE MUMBAI;
CREATE TABLE PUNE LIKE MUMBAI;