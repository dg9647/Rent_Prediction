CREATE DATABASE IF NOT EXISTS PREPROCESS;
USE PREPROCESS;
SET SQL_REQUIRE_PRIMARY_KEY = OFF;
CREATE TABLE MUMBAI(
ID INTEGER PRIMARY KEY AUTO_INCREMENT,
SELLER_TYPE FLOAT, 
BEDROOM FLOAT, 
LAYOUT FLOAT, 
PROPERTY_TYPE FLOAT, 
LOCALITY FLOAT, 
PRICE FLOAT, 
AREA FLOAT, 
FURNISH_TYPE FLOAT, 
BATHROOM FLOAT
);


CREATE TABLE DELHI LIKE MUMBAI;
CREATE TABLE KOLKATA LIKE MUMBAI;
CREATE TABLE BANGALORE LIKE MUMBAI;
CREATE TABLE HYDERABAD LIKE MUMBAI;
CREATE TABLE CHENNAI LIKE MUMBAI;
CREATE TABLE AHEMDABAD LIKE MUMBAI;
CREATE TABLE PUNE LIKE MUMBAI;